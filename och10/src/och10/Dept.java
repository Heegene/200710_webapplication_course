package och10;

public class Dept {
	
	private int detpno;
	private String dname;
	private String loc;
	
	public int getDetpno() {
		return detpno;
	}
	public void setDetpno(int detpno) {
		this.detpno = detpno;
	}
	public String getDname() {
		return dname;
	}
	public void setDname(String dname) {
		this.dname = dname;
	}
	public String getLoc() {
		return loc;
	}
	public void setLoc(String loc) {
		this.loc = loc;
	}
	
	
	

}
